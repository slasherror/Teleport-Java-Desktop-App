    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String url = "jdbc:mysql://localhost:3306/mysql";
        String user = "root";
        String password = "anik"; 

        try (Connection connection = DriverManager.getConnection(url, user, password)) {
            String query = "SELECT * FROM Admin_login1 WHERE UserId = ? AND Password = ?";
            PreparedStatement preparedStatement = connection.prepareStatement(query);
            preparedStatement.setString(1, jTextField3.getText()); 
            preparedStatement.setString(2, new String(jPasswordField1.getPassword())); 

            ResultSet resultSet = preparedStatement.executeQuery();

            if (resultSet.next()) {
                
                System.out.println("Login successful");
                 new Admins().setVisible(true);
                 setVisible(false);
                  dispose();
                
                
            } else {
               
                System.out.println("Login failed");
                JOptionPane.showMessageDialog(this, "Wrong UserId or Password");
            }
        } catch (SQLException e) {
            // Handle any errors
            e.printStackTrace();
        }
    
    }//GEN-LAST:event_jButton1ActionPerformed

